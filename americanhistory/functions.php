// Добавляем изображение через описание term
function add_taxonomy_thumbs( $out, $term ) {
    if($term->description){
        echo '<img src="'.strip_tags($term->description).'" style="margin:0 10px 3px 0;float:left;" width="48px" height="48px" />';
    }
    return $out;
}
 
add_filter( 'author_row_actions', 'add_taxonomy_thumbs', 10, 2 );

